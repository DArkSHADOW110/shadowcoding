#include <stdio.h>
int main(){
  //this is one line comment
  /*this is a 
    multi line 
    comment*/
  printf("Hello World\n");
  printf("Hello World\n");
  //(\n)means New line
  printf("Hello   World\n");
  printf("Hello\tWorld\n");
  //(\t)means 4 spaces
  printf("Hello \"World\"\n");
  //display to double quatation
  printf("Hello\b world\n");
  //(\b)means backspace
  printf("Hello\rWorld\n");
  //(\r) means all characters backspace that statement only
  printf("Hello\\ world\n");
  printf("Hello\\\\ world\n");
  printf("Hello\/ world\n");
  return 0;
}